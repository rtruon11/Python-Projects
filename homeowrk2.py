# Title: Program converts miles per hour to feet per second
# Description: User enters miles per hour value, program converts miles per hour to feet per second, prints the feet per second answer


miles_per_hour = int(input('Enter miles per hour ')) # user enters miles per hour value
feet_per_second = miles_per_hour/1.46667 # converts miles per hour to feet per second
print (miles_per_hour, 'miles per hour=', feet_per_second, 'feet per second')


